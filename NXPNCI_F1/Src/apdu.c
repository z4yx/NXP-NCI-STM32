static uint16_t update_binary(uint8_t sfi, uint8_t offset, uint8_t len, uint8_t* data)
{
    uint16_t flen;
    uint8_t* fp;
    get_fp(sfi, &fp, &flen);
    printf("W/ %02x %d+%d<=%d\r\n", sfi, offset, len, flen);
    if(fp && offset+len <= flen){
        memcpy(fp+offset, data, len);
        return 0x9000;
    }else{
        return 0x6a82;
    }
}

static uint16_t read_binary(uint8_t sfi, uint8_t offset, uint8_t len, uint8_t* data)
{
    uint16_t flen;
    uint8_t* fp;
    get_fp(sfi, &fp, &flen);
    printf("R/ %02x %d+%d<=%d\r\n", sfi, offset, len, flen);
    if(fp && offset+len <= flen){
        memcpy(data, fp+offset, len);
        return 0x9000;
    }else{
        return 0x6a82;
    }
}

void handleAPDU(uint8_t* recv, uint8_t sz_recv, uint8_t* send, uint8_t* psz_send)
{
    uint8_t ins = recv[1],
            p1 = recv[2],
            p2 = recv[3],
            lc = recv[4];
    uint8_t* data = recv+5;
    uint16_t status = 0x6D00; //(Instruction code not supported or invalid)
    *psz_send = 0;

    switch (ins)
    {
        case 0x84:
            for(int i = 0; i < 4; i++)
                send[i] = i;
            *psz_send = 4;
            status = 0x9000;
            break;
        case 0x88:
            printf("int auth %02x %02x %02x %02x\r\n",
                data[0],data[1],data[2],data[3]);
            for(int i = 0; i < 8; i++)
                send[i] = i;
            *psz_send = 8;
            status = 0x9000;
            break;
        case 0xA4:
            status = 0x6A82;
            if(p1 == 4){ // By DF Name
                if(lc==9 && memcmp(data, "\xA0\x00\x00\x00\x03\x86\x98\x07", 8)==0){
                    if(data[8] == 1){
                        memcpy(send, fci_3f01, sizeof(fci_3f01));
                        *psz_send = sizeof(fci_3f01);
                        fid=0x3f01;
                        status = 0x9000;
                    }
                    else if(data[8] == 4){
                        memcpy(send, fci_3f04, sizeof(fci_3f04));
                        *psz_send = sizeof(fci_3f04);
                        fid=0x3f04;
                        status = 0x9000;
                    }
                }
            }else if(p1 == 0){ // By file identifier
                if(lc == 0){
                    fid = 0x3f00;
                    status = 0x9000;
                }
            }
            break;
        case 0xD6:
            if(p1&0x80){
                uint8_t sfi = p1&0x1f;
                status = update_binary(sfi, p2, lc-4, data);
            }
            break;
        case 0xB0:
            if(p1&0x80){
                uint8_t sfi = p1&0x1f;
                status = read_binary(sfi, p2, lc, send);
                if(status == 0x9000)
                    *psz_send = lc;
            }
            break;
        default:
            break;
    }
    send[(*psz_send)++] = (status>>8) & 0xff;
    send[(*psz_send)++] = status & 0xff;
    printf("apdu %02x %02x %02x %02x %04x\r\n",
        ins, p1, p2, lc, status);
}
